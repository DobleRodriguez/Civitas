    public void setCasilla(Casilla casilla, int numero) {
        labelPrecio.setVisible(false);
        labelPropietario.setVisible(false);
        textfieldPrecio.setVisible(false);
        textfieldPropietario.setVisible(false);
        textfieldNombre.setText(casilla.getNombre());
        textfieldNumero.setText(String.valueOf(numero));
        textfieldTipo.setText(casilla.getTipo());
        if (casilla.getTipo().equals("Calle")) {
            TituloPropiedad propiedad = ((CasillaCalle)casilla).getTituloPropiedad();
            if (propiedad.tienePropietario()) {
                textfieldPropietario.setText(propiedad.getPropietario().getNombre());
                labelPrecio.setText("Precio de alquiler");
                textfieldPrecio.setText(String.valueOf(propiedad.getPrecioAlquiler()));
                labelPropietario.setVisible(true);
                textfieldPropietario.setVisible(true);
            } else {
                labelPrecio.setText("Precio de compra");
                textfieldPrecio.setText(String.valueOf(propiedad.getPrecioCompra()));
            } 
            labelPrecio.setVisible(true);
            textfieldPrecio.setVisible(true);
        } 
    }

